const questions = computed(() => [
  {
    id: 'q1',
    title: t.value.q1Title,
    options: [
      { value: 'beginner', label: t.value.q1O1 },
      { value: 'mid', label: t.value.q1O2 },
      { value: 'expert', label: t.value.q1O3 }
    ]
  },
  {
    id: 'q2',
    title: t.value.q2Title,
    options: [
      { value: 'index', label: t.value.q2O1 },
      { value: 'macro', label: t.value.q2O2 },
      { value: 'dividend', label: t.value.q2O3 },
      { value: 'mindset', label: t.value.q2O4 }
    ]
  },
  {
    id: 'q3',
    title: t.value.q3Title,
    options: [
      { value: 'no_rule', label: t.value.q3O1 },
      { value: 'mental', label: t.value.q3O2 },
      { value: 'economy', label: t.value.q3O3 },
      { value: 'cashflow', label: t.value.q3O4 }
    ]
  }
])

const step = ref(0)
const answers = ref({
  q1: '',
  q2: '',
  q3: ''
})

const selectOption = (value: string) => {
  if (step.value === 0) answers.value.q1 = value
  else if (step.value === 1) answers.value.q2 = value
  else if (step.value === 2) answers.value.q3 = value
  
  if (step.value < 2) {
    step.value++
  } else {
    step.value = 3
    calculateRecommendations()
  }
}

const resetQuiz = () => {
  step.value = 0
  answers.value = { q1: '', q2: '', q3: '' }
  recommendedBooks.value = []
}

interface RecommendedBook extends Book {
  reason: string;
}

const recommendedBooks = ref<RecommendedBook[]>([])

// Recalculate if lang changes while results are showing
watch(lang, () => {
  if (step.value === 3) {
    calculateRecommendations()
  }
})

const calculateRecommendations = () => {
  let scoredBooks = books.value.map(b => ({ ...b, score: 0, reason: '' }))
  
  const { q1, q2, q3 } = answers.value

  scoredBooks.forEach(b => {
    const isEn = lang.value === 'en-US'
    const cat = b.category || ''
    const ts = (b.tags || []).join(',')
    
    // Check based on lang
    if (q2 === 'index' && (cat.includes('мқёлҚұмҠӨ') || cat.includes('Index'))) b.score += 10
    if (q2 === 'macro' && (cat.includes('кұ°мӢң кІҪм ң') || cat.includes('Macro'))) b.score += 10
    if (q2 === 'dividend' && (cat.includes('нҳ„кёҲ нқҗлҰ„') || cat.includes('Dividend'))) b.score += 10
    if (q2 === 'mindset' && (cat.includes('нҲ¬мһҗ мӢ¬лҰ¬') || cat.includes('Mindset'))) b.score += 10

    if (q3 === 'no_rule' && (ts.includes('мһҗмӮ°л°°л¶„') || ts.includes('Asset Allocation') || ts.includes('ETF') || ts.includes('к°Җм№ҳ нҲ¬мһҗ') || ts.includes('Value'))) b.score += 8
    if (q3 === 'mental' && (ts.includes('мӢ¬лҰ¬') || ts.includes('Psychology') || ts.includes('мқёк°„ н–үлҸҷн•ҷ') || ts.includes('Behavioral') || ts.includes('мһҘкё° нҲ¬мһҗ') || ts.includes('Long-term'))) b.score += 8
    if (q3 === 'economy' && (ts.includes('л§ӨнҒ¬лЎң') || ts.includes('Macro') || ts.includes('кёҲлҰ¬') || ts.includes('Interest') || ts.includes('лӢ¬лҹ¬') || ts.includes('Dollar'))) b.score += 8
    if (q3 === 'cashflow' && (ts.includes('л°°лӢ№ м„ұмһҘ') || ts.includes('Dividend Growth') || ts.includes('мһҗлҸҷ мҶҢл“қ') || ts.includes('Passive Income') || ts.includes('лҜёкөӯ л°°лӢ№') || ts.includes('US Dividend'))) b.score += 8

    const isEasy = b.title.includes('мІҳмқҢ') || b.title.includes('мү¬мҡҙ') || b.description.includes('мҙҲліҙ') || b.title.includes('л§ҲлІ•мқҳ') || b.title.includes('Magic') || b.title.includes('Little') || b.description.includes('beginner')
    const isHard = b.title.includes('кіөмӢқ') || b.description.includes('лҚ°мқҙн„°') || b.description.includes('л§Өм»ӨлӢҲмҰҳ') || b.title.includes('Formula') || b.description.includes('data')

    if (q1 === 'beginner' && isEasy) b.score += 5
    if (q1 === 'beginner' && isHard) b.score -= 3
    if (q1 === 'expert' && isHard) b.score += 5
    
    if (b.badge?.includes('Favorite') || b.badge?.includes('мқёмғқ лҸ„м„ң')) b.score += 2
    if (b.badge?.includes('Recommendation') || b.badge?.includes('м¶”мІң лҸ„м„ң')) b.score += 1
  })

  scoredBooks.sort((a, b) => b.score - a.score)
  const top3 = scoredBooks.slice(0, 3)
  
  top3.forEach((b, index) => {
    let reason = ''
    if (index === 0) reason = t.value.reason1
    else if (index === 1) reason = t.value.reason2
    else reason = t.value.reason3
    
    if (q2 === 'index') reason += t.value.reasonAppendIndex
    else if (q2 === 'macro') reason += t.value.reasonAppendMacro
    else if (q2 === 'dividend') reason += t.value.reasonAppendDividend
    else if (q2 === 'mindset') reason += t.value.reasonAppendMindset
    
    b.reason = reason
  })
  
  recommendedBooks.value = top3
}
</script>

