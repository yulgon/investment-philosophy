              <p class="author">{{ book.author }}</p>
              
              <div class="reason-box">
                <strong>{{ t.reasonTitle }}</strong>
                <p>{{ book.reason }}</p>
              </div>
              
              <p class="description">{{ book.description }}</p>
              
              <div v-if="book.myComment" class="my-comment">
                <strong>{{ t.myCommentTitle }}</strong> {{ book.myComment }}
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

