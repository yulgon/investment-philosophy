      <div class="progress-bar">
        <div class="progress" :style="{ width: `${((step) / 3) * 100}%` }"></div>
      </div>
      <p class="step-indicator">{{ t.stepIndicator }} {{ step + 1 }} {{ t.of }} 3</p>
      
      <h2 class="question-title">{{ questions[step].title }}</h2>
      
      <div class="options-grid">
        <button 
